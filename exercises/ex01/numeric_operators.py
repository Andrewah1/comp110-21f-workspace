Left_hand_side = int(input("enter value for Left-hand side: "))
Right_hand_side = int(input("enter value for Right_hand_side: "))

print(Left_hand_side, "**", Right_hand_side, "is", (Left_hand_side ** Right_hand_side))
print(Left_hand_side, "/", Right_hand_side, "is", (Left_hand_side / Right_hand_side))
print(Left_hand_side, "//", Right_hand_side, "is", (Left_hand_side // Right_hand_side))
print(Left_hand_side, "%", Right_hand_side, "is", (Left_hand_side % Right_hand_side))
 