Left_hand_side = int(input("Left-hand side: "))
Right_hand_side = int(input("Right-hand side: "))

print(Left_hand_side, "<", Right_hand_side, "is", (Left_hand_side < Right_hand_side))
print(Left_hand_side, ">=", Right_hand_side, "is", (Left_hand_side >= Right_hand_side))
print(Left_hand_side, "==", Right_hand_side, "is", (Left_hand_side == Right_hand_side))
print(Left_hand_side, "!=", Right_hand_side, "is", (Left_hand_side != Right_hand_side))
